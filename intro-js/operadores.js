// operadores aritméticos
console.log("operadores aritméticos")

// adição +
var soma = 1+1;
console.log(soma)
// subtração -
var sub = 2 - 1;
console.log(sub)
// multiplicação *
var multi = 2*2
console.log(multi)
// divisão /
var divisao = 4 / 2
console.log(divisao)
// módulo ou resto de uma divisão %
var resto = 8 % 2
console.log(resto)
// incremento ++
var num = 1;
console.log(num)
console.log(++num)
num = num + 1
console.log(num)
// descremento --
var num = 2;
console.log(num)
console.log(--num)
num = num - 1
console.log(num)



